import math

gra35 = math.radians(35)
sin35 = math.sin(gra35)
cos35 = math.cos(gra35)
print('sin 35', sin35)
print('cos 35', cos35)
print('')

sin35sq = math.pow(sin35, 2)
cos35sq = math.pow(cos35, 2)
print('sin 35 quare', sin35sq)
print('cos 35 quare', cos35sq)

print('sin35^2 + cos35^2 =', round(sin35sq +cos35sq, 10))